import { buttonVariants } from '@/components/ui/button'
import { Separator } from '@/components/ui/separator'
import CommentSection from '@/components/web/CommentSection'
import PostPresence from '@/components/web/PostPresence'
import { api } from '@/convex/_generated/api'
import { Id } from '@/convex/_generated/dataModel'
import { getToken } from '@/lib/auth-server'
import { fetchQuery, preloadQuery } from 'convex/nextjs'
import { ArrowLeft } from 'lucide-react'
import { Metadata } from 'next'
import Image from 'next/image'
import Link from 'next/link'
import { redirect } from 'next/navigation'

interface PostIdPageProps {
  params: Promise<{ postId: Id<'blogs'> }>
}

export async function generateMetadata({
  params,
}: PostIdPageProps): Promise<Metadata> {
  const { postId } = await params
  const post = await fetchQuery(api.blogs.getPostById, { postId: postId })

  if (!post) {
    return {
      title: 'Post Not Found | Next.js + Convex Starter',
    }
  }

  return {
    title: post.title,
    description: post.content,
  }
}

const PostIdPage = async ({ params }: PostIdPageProps) => {
  const { postId } = await params

  const token = await getToken()

  const [post, preloadedComments, userId] = await Promise.all([
    await fetchQuery(api.blogs.getPostById, { postId: postId }),
    await preloadQuery(api.comments.getCommentsByPostId, {
      postId: postId,
    }),
    await fetchQuery(api.presence.getUserId, {}, { token }),
  ])

  if (!userId) {
    return redirect('/auth/login')
  }

  if (!post) {
    return (
      <div className='text-6xl font-extrabold text-red-500 p-20'>
        <h1>No Post Found</h1>
      </div>
    )
  }

  return (
    <div className='max-w-3xl mx-auto py-8 px-4 animate-in fade-in duration-500 relative'>
      <Link
        href='/blog'
        className={buttonVariants({
          variant: 'ghost',
          className: 'mb-4',
        })}
      >
        <ArrowLeft className='size-4' />
        Back to blog
      </Link>

      <div className='relative w-full h-100 mb-8 rounded-xl overflow-hidden shadow-sm'>
        <Image
          src={
            post.imageUrl ??
            'https://images.unsplash.com/photo-1770567351994-dd4a743ab1e6?q=80&w=1470&auto=format&fit=crop&ixlib=rb-4.1.0&ixid=M3wxMjA3fDB8MHxwaG90by1wYWdlfHx8fGVufDB8fHx8fA%3D%3D'
          }
          alt='PostImage'
          fill
          className='object-cover hover:scale-105 transition-transform duration-500'
        />
      </div>

      <div className='space-y-4 flex flex-col'>
        <h1 className='text-4xl font-bold tracking-tight text-foreground'>
          {post.title}
        </h1>

        <div className='flex items-center gap-4'>
          <p className='text-sm text-muted-foreground'>
            Posted on:{' '}
            {new Date(post._creationTime).toLocaleDateString('en-US')}
          </p>

          {userId && <PostPresence roomId={postId} userId={userId} />}
        </div>
      </div>

      <Separator className='my-8' />

      <p className='text-lg leading-relaxed text-foreground/90 whitespace-pre-wrap'>
        {post.content}
      </p>

      <Separator className='my-8' />

      <CommentSection preloadedComments={preloadedComments} />
    </div>
  )
}

export default PostIdPage
